void print_ctr_display(WINDOW *control, byte num) {
  wattron(control, COLOR_PAIR(9));
  for (int i = 0; i < 4; i++)
    for (int j = 0; j < 3; j++)
      mvwprintw(control, i + 15, j + WIDTH_CONTROL / 2 - 3, "%c",
                clock_face((num / 10) % 10, i, j));

  for (int i = 0; i < 4; i++)
    for (int j = 0; j < 3; j++)
      mvwprintw(control, i + 15, j + WIDTH_CONTROL / 2, "%c",
                clock_face(num % 10, i, j));

  if (num >= 50) mvwprintw(control, 15 + 3, WIDTH_CONTROL / 2, "%s", "MAX");
  if (num <= 2) mvwprintw(control, 15 + 3, WIDTH_CONTROL / 2 - 3, "%s", "MIN");
  wattroff(control, COLOR_PAIR(9));
}

void print_ctr_display_board(WINDOW *control) {
  wattron(control, COLOR_PAIR(9));
  for (int i = 0; i < 4; i++) {
    mvwprintw(control, i + 15, WIDTH_CONTROL / 2 - 4, "%s", " ");
    mvwprintw(control, i + 15, WIDTH_CONTROL / 2 + 3, "%s", " ");
  }
  wattroff(control, COLOR_PAIR(9));
}

